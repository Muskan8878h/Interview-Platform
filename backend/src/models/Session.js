import mongoose from "mongoose";

const sessionSchema = new mongoose.Schema(
  {
    problem: {
      type: String,
      required: true,
    },

    difficulty: {
      type: String,
      enum: ["easy", "medium", "hard"],
      required: true,
    },

    hostClerkId: {
      type: String,
      required: true,
    },

    participantClerkId: {
      type: String,
      default: null,
    },

    status: {
      type: String,
      enum: ["active", "completed"],
      default: "active",
    },

    callId: {
      type: String,
    },
  },
  { timestamps: true }
);

const Session = mongoose.model("Session", sessionSchema);

